// ============================================================================
// ip_filter.h — 네트워크 IP 화이트리스트
// ============================================================================
// 목적:
//   외부망 접근을 차단하고 내부망에서만 서버에 접속 가능하도록 제한한다.
//
// 허용 대역:
//   - 127.0.0.1        : 로컬호스트
//   - 10.0.0.0/8       : 사설 A 클래스
//   - 172.16.0.0/12    : 사설 B 클래스 (172.16~31)
//   - 192.168.0.0/16   : 사설 C 클래스
// ============================================================================
#pragma once

#include <cstdlib>
#include <string>

namespace factory::security {

/// 내부망 IP인지 검증
/// @param ip "192.168.1.10" 형식의 점으로 구분된 IPv4
inline bool is_allowed_ip(const std::string& ip) {
    // 로컬호스트: 같은 PC에서 접속한 경우
    if (ip == "127.0.0.1") return true;

    // 10.x.x.x: Class A 사설 대역 (16,777,216개 주소, 대규모 네트워크)
    // rfind(needle, 0): 위치 0에서 시작하는 부분 문자열 매치 (= starts_with)
    //   C++17에는 starts_with가 없어서 rfind로 대체 (C++20부터 starts_with 사용 가능)
    if (ip.rfind("10.", 0) == 0) return true;

    // 192.168.x.x: Class C 사설 대역 (65,536개 주소, 일반 가정/소규모 오피스)
    if (ip.rfind("192.168.", 0) == 0) return true;

    // 172.16.x.x ~ 172.31.x.x: Class B 사설 대역 (1,048,576개 주소)
    // "172." 로 시작하면 두 번째 옥텟을 파싱해서 16~31 범위인지 확인
    if (ip.rfind("172.", 0) == 0) {
        // c_str() + 4 = "172." 이후 위치 (예: "172.16.0.1" → "16.0.1")
        // atoi는 숫자 아닌 첫 문자에서 중단 → "16.0.1" → 16 반환
        int second = std::atoi(ip.c_str() + 4);
        if (second >= 16 && second <= 31) return true;
    }

    // 위 조건 모두 불일치: 외부망 IP (공격자 가능성) → 차단
    return false;
}

} // namespace factory::security
